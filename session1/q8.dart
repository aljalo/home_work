// Question 8: What is the difference between the num and double data types in Dart?

// Answer: In 'num' we can represent both integer and double values.
// But in double we can represent only double values.

void main() {
  num number1 = 10;
  num number2 = 5.5;
  double number3 = 7.8;

  int number = 15;
  double num2 =
      number; // We can`t assign value of type integer to variable of type double.
}
